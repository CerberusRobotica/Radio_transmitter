  HAL_UART_Transmit(&huart1, (uint8_t*)welcome_msg, strlen(welcome_msg), 100); // 100ms de timeout

  for (int i = 0; i < 16; i++) {
      pct_robo[i].id = i;
      pct_robo[i].Vx = 0;
      pct_robo[i].Vy = 0;
      pct_robo[i].Vang = 0;
      pct_robo[i].kicker = 0;
      pendente[i] = 0;
  }


  HAL_UART_Receive_IT(&huart1, usart_rx, sizeof(usart_rx));  // primeira chamada

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
